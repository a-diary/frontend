const extendMarkdown = text => {
    for (let i = 0; i < emojis.length; i++) {
        const emoji = emojis[i],
            reg = new RegExp(`\\[${emoji}\\]`, "g");
        text = text.replaceAll(
            reg,
            `<img class="markdown-emoji" src="https://pubcdn.yxzl.top/emojis/${emoji}.png" alt="${emoji}" title="${emoji}" />`
        );
    }
    return text;
};

export { extendMarkdown };
